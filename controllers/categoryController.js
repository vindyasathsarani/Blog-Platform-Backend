// @desc    Update category
// @route   PUT /api/categories/:id
// @access  Admin
exports.updateCategory = async (req, res) => {
  try {
    const { name, description } = req.body;
    
    const category = await Category.findById(req.params.id);

    if (!category) {
      return res.status(404).json({ message: 'Category not found' });
    }

    category.name = name || category.name;
    category.description = description || category.description;

    const updatedCategory = await category.save();
    res.json(updatedCategory);
  } catch (error) {
    res.status(500).json({ message: error.message });
  }
};

// @desc    Delete category
// @route   DELETE /api/categories/:id
// @access  Admin
exports.deleteCategory = async (req, res) => {
  try {
    const category = await Category.findById(req.params.id);

    if (!category) {
      return res.status(404).json({ message: 'Category not found' });
    }

    // Check if there are posts using this category
    const postsWithCategory = await Post.countDocuments({ category: category.name });
    
    if (postsWithCategory > 0) {
      return res.status(400).json({ 
        message: `Cannot delete category. ${postsWithCategory} posts are using this category.` 
      });
    }

    await category.deleteOne();
    res.json({ message: 'Category removed' });
  } catch (error) {
    res.status(500).json({ message: error.message });
  }
};

// @desc    Get posts by category
// @route   GET /api/categories/:id/posts
// @access  Public
exports.getPostsByCategory = async (req, res) => {
  try {
    const category = await Category.findById(req.params.id);
    
    if (!category) {
      return res.status(404).json({ message: 'Category not found' });
    }
    
    const posts = await Post.find({ category: category.name })
      .sort({ createdAt: -1 })
      .populate('author', 'name email profilePicture');
      
    res.json(posts);
  } catch (error) {
    res.status(500).json({ message: error.message });
  }
};